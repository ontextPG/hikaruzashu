package jp.co.aa.milab.model;

public class Employee {
	
	private String empId;
	private String empName;
	private String pass;
	private String totalWorkHrs;
	private String mail;
	private String dateJoin;
	private String positionId;
	private String positionName;
	private String depaId;
	private String depaName;
	
	
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getTotalWorkHrs() {
		return totalWorkHrs;
	}
	public void setTotalWorkHrs(String totalWorkHrs) {
		this.totalWorkHrs = totalWorkHrs;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getDateJoin() {
		return dateJoin;
	}
	public void setDateJoin(String dateJoin) {
		this.dateJoin = dateJoin;
	}
	public String getPositionId() {
		return positionId;
	}
	public void setPositionId(String positionId) {
		this.positionId = positionId;
	}
	public String getPositionName() {
		return positionName;
	}
	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}
	public String getDepaId() {
		return depaId;
	}
	public void setDepaId(String depaId) {
		this.depaId = depaId;
	}
	public String getDepaName() {
		return depaName;
	}
	public void setDepaName(String depaName) {
		this.depaName = depaName;
	}

}
